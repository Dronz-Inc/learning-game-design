def trait_masks():
    """{key: (group, label, bitmask)} for exploration scripts."""
    return {k: (g, lbl, B.mask_of(p)) for (k, g, lbl, p) in TRAITS}


def main():
    path = os.path.join(HERE, "traits_matrix.csv")
    with open(path, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["Country", "Continent", "Subregion", "Population", "Area_km2",
                    "Landlocked", "Neighbours", "Peak_m"]
                   + [f"[{g}] {lbl}" for (_, g, lbl, _) in TRAITS])
        for nm in NAMES:
            at = a(nm)
            base = [nm, at["continent"], at["subregion"], at["pop"], at["area"],
                    at["landlocked"], at["neighbours"], at["peak"]]
            w.writerow(base + ["Y" if p(nm) else "" for (_, _, _, p) in TRAITS])
    print(f"Wrote {path}  ({len(NAMES)} countries x {len(TRAITS)} traits)\n")
    print("Trait breadths (count / 193); * = outside the ~8%-45% sweet spot:")
    grp = None
    for k, g, lbl, p in TRAITS:
        if g != grp:
            grp = g
            print(f"  -- {g} --")
        c = sum(1 for n in NAMES if p(n))
        flag = "" if 15 <= c <= 87 else " *"
        print(f"     {lbl:38s} {c:3d} ({c/len(NAMES)*100:2.0f}%){flag}")


if __name__ == "__main__":
    main()
