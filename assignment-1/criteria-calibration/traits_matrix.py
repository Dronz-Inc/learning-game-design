def _load():
    d = {}
    with open(os.path.join(HERE, "country_data.csv")) as f:
        for r in csv.DictReader(f):
            nm = r["Country"]
            d[nm] = dict(
                capital=r["Capital"],
                pop=int(r["Population"]),
                area=int(r["Area_km2"]),
                continent=r["Continent"],
                subregion=r["Subregion"],
                landlocked=(r["Landlocked"] == "Yes"),
                neighbours=int(r["Bordering_countries"]),
                coasts=set() if r["Coasts"] == "none" else {x.strip() for x in r["Coasts"].split(";")},
                peak=int(r["Highest_peak_m"]),
                drives=r["Drives_on"],
                langs=[x.strip() for x in r["Official_languages"].split(";")],
                currency=r["Currency"],
                government=r["Government"],
                hemisphere=r["Hemisphere"],
                flag_colors={x.strip() for x in r["Flag_colors"].split(";")},
                flag_symbols=set() if r["Flag_symbols"] == "none" else {x.strip() for x in r["Flag_symbols"].split(";")},
                archipelago=(r["Archipelago"] == "Yes"),
                eu=(r["EU_member"] == "Yes"),
                commonwealth=(r["Commonwealth_member"] == "Yes"),
            )
    return d

